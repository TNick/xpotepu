logic_template_t * create_test_template()
{
    xpotepu_logic_t * parent = create_test_logic();
    logic_template_t *tpl;
    logic_template_new (parent, &tpl);
    return tpl;
}

void add_n_templates (xpotepu_logic_t * parent, int n)
{
    logic_template_t *tpl;
    int i;
    for (i = 0; i < n; ++i) {
        logic_template_new (parent, &tpl);
    }
}

void compare_two_texts(const char * t1, const char * t2) {

    char c;
    for (;;) {
        c = *t1++;
        EXPECT_EQ(c, *t2++);
        if (c == 0) {
            break;
        }
    }

}

